document.getElementById("show-div-1").addEventListener("click", () => {
    var display = document.getElementById("div-1").style.display;
    if(display == "none"){
        document.getElementById("div-1").style.display = 'block';
    }else{
        document.getElementById("div-1").style.display = 'none';
    }
})

